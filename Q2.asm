ORG 0000H
MOV 50H, #10H
MOV 51H,#12H
MOV A, 50H
MOV R0, A
MOV A, 51H
MOV R1, A
CLR A
MOV 52H, A

CHECK:
MOV A, R0
JZ R0_ZERO
MOV A, R1
JZ R1_ZERO
DEC R0
DEC R1
SJMP CHECK

R0_ZERO:
MOV A, R1
JZ EQUAL
MOV A, #0FFH
MOV 52H, A
SJMP ENDPROG

R1_ZERO:
MOV A, #01H
MOV 52H, A
SJMP ENDPROG

EQUAL:
CLR A
MOV 52H, A

ENDPROG:
SJMP ENDPROG

END
